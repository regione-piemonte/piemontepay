		Object cpWidgetStatus = session.get("cpHomePage");
		log.debug("[CpHomePageAction::dumpmodel] " + cpWidgetStatus);
		log.debug("[CpHomePageAction::dumpmodel] [c] sessione");
		log.debug("[CpHomePageAction::dumpmodel] " + session);
